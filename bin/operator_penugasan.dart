void main() {
  var a = 10; // Inisialisasi nilai awal

  // Menambahkan 10
  a += 10; // Sekarang a = 20
  print('Setelah a += 10: $a');

  // Mengurangi 10
  a -= 10; // Sekarang a = 10
  print('Setelah a -= 10: $a');

  // Mengalikan dengan 10
  a *= 10; // Sekarang a = 100
  print('Setelah a *= 10: $a');

  // Menambahkan 10 dua kali
  a += 10; // Sekarang a = 110
  print('Setelah a += 10: $a');

  a += 10; // Sekarang a = 120
  print('Setelah a += 10: $a');
}
